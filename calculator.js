let firstNmber = 5;
let secondNmber =7;
let operation ="*";
if( operation==="+"){
    console.log(`${firstNmber}+${secondNmber}=${firstNmber+secondNmber}`)
}else if (operation==="-"){ 
   console.log(`${firstNmber}-${secondNmber}=${firstNmber-secondNmber}`)  
}else if (operation==="*"){
     console.log(`${firstNmber}*${secondNmber}=${firstNmber*secondNmber}`)
}else if (operation==="/"){
 console.log(`${firstNmber}/${secondNmber}=${firstNmber/secondNmber}`)
}else console.log("Error operation")